package pojo;

/**
 * Questa classe modella il concetto di "servizi offerti da un aula" all'interno sistema
 *
 * @author De Caro Antonio
 * @version 0.1
 */
public enum Servizio {
    /**
     * Se associato ad un aula, questa costante indica che l'aula mette a disposizione delle prese di corrente.
     */
    PRESE,

    /**
     * Se associato ad un aula, questa costante indica che l'aula mette a disposizione delle postazioni con PC.
     */
    COMPUTER
}
